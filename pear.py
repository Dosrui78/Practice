BASE_URL = 'https://www.pearvideo.com'
def scrape_url(url):
    logging.info('scraping %s...', url)
    try:
        res = requests.get(url, headers=header)
        return res.text
    except TimeoutError as a:
        logging.info(f'Error time is Out:{a}')
    except ReadTimeout as b:
        logging.info(f'Error ReadTimeout:{b}')
    except ConnectionError as c:
        logging.info(f'Error ConnectionError:{c}')
    except HTTPError as d:
        logging.info(f'Error URLError:{d}')


def scrape_index(url):
    #logging.info('scraping index %s', url)
    return scrape_url(url)


def parse_index(content):
    sel = Selector(content)
    lists = sel.css('.act-main.cmmain')
    #all_tags = []
    for list in lists:
        big_url = list.css('div.vervideo-blist-big > div.vervideo-bd > a::attr("href")').getall()
        #yield big_url
        small_url1 = list.css('div.vervideo-blist-small > ul > li > div > a::attr("href")').getall()
        #print(small_url1)
        small_url2 = list.css('div.vervideo-blist-small.pcshow > ul > li > div > a::attr("href")').getall()
        #print(small_url2)
        all_tags = big_url + small_url1 +small_url2
        print(len(all_tags))
        return all_tags

def scrape_detail(tag):
    detail_url = f'{BASE_URL}/{tag}'
    logging.info('getting detail %s', detail_url)
    return scrape_url(detail_url)

def parse_detail(html):
    sel = Selector(html)
    global title
    title = sel.css('#detailsbd > div.details-main.vertical-details.cmmain > div:nth-child(4) > div > div.box-left.clear-mar > h1::text').get()
    logging.info('The Video Title: %s', title)
    pattern = re.compile('srcUrl="(.*?.mp4)"')
    video_url = re.findall(pattern, html)[0]
    logging.info('The Video link:%s', video_url)
    content = requests.get(video_url, headers=header).content
    path = 'PEAR_VIDEO'
    if not os.path.exists(path):
        os.mkdir(path)
    name = video_url.split('/')[-1]
    file_path = path + '/' + name
    with open(file_path, 'wb') as f:
        f.write(content)
        logging.info(f"{title}Download Successfully!")

